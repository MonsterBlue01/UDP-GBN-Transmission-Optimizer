Problem 3: P (MM) = 0.4, P (MW) = 0.5 and P (MM|MW) = 0.7
            (a): P (MM and MW) = 0.5 * 0.7 = 0.35
            (b): P (MW|MM) = P(MM and MW) / P(MM) = 0.35 / 0.4 = 0.875
            (c): P (MM or MW) = P (MW) + P (MM) − P (MM and MW) = 0.4 + 0.5 − 0.35 = 0.55.            
